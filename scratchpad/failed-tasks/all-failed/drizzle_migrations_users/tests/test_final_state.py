def test_init_migration_file_exists():
    assert os.path.isfile(
        INIT_MIGRATION
    ), f"Expected initial migration file at {INIT_MIGRATION}"


def test_init_migration_creates_users_table():
    with open(INIT_MIGRATION, "r", encoding="utf-8") as f:
        sql = f.read()
    assert re.search(
        r"create\s+table[^;]*users", sql, re.IGNORECASE | re.DOTALL
    ), f"Initial migration must CREATE TABLE users; got:\n{sql}"
    for keyword in ("email", "password_hash", "created_at"):
        assert re.search(
            rf"\b{keyword}\b", sql, re.IGNORECASE
        ), f"Initial migration must reference column '{keyword}'."
    assert re.search(
        r"primary\s+key", sql, re.IGNORECASE
    ), "Initial migration must declare a PRIMARY KEY on users."
    assert re.search(
        r"autoincrement", sql, re.IGNORECASE
    ), "Initial migration must use AUTOINCREMENT for the users id."
    assert re.search(r"unique", sql, re.IGNORECASE) or re.search(
        r"create\s+unique\s+index", sql, re.IGNORECASE
    ), "Initial migration must enforce UNIQUE on users.email."


def test_add_display_name_migration_file_exists():
    assert os.path.isfile(
        ADD_COL_MIGRATION
    ), f"Expected second migration file at {ADD_COL_MIGRATION}"


def test_add_display_name_migration_alters_users():
    with open(ADD_COL_MIGRATION, "r", encoding="utf-8") as f:
        sql = f.read()
    assert re.search(
        r"alter\s+table[^;]*users[^;]*add\s+column",
        sql,
        re.IGNORECASE | re.DOTALL,
    ), f"Second migration must ALTER TABLE users ADD COLUMN; got:\n{sql}"
    assert re.search(
        r"display_name", sql, re.IGNORECASE
    ), "Second migration must add the display_name column."
    assert re.search(
        r"\btext\b", sql, re.IGNORECASE
    ), "display_name must be declared TEXT."



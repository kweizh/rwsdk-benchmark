import os
import re
import socket
import time

import pytest
import requests
from xprocess import ProcessStarter

PROJECT_DIR = "/home/user/myproject"
BASE_URL = "http://localhost:5173"
MIGRATIONS_DIR = os.path.join(PROJECT_DIR, "drizzle", "migrations")
INIT_MIGRATION = os.path.join(MIGRATIONS_DIR, "0000_init.sql")
ADD_COL_MIGRATION = os.path.join(MIGRATIONS_DIR, "0001_add_display_name.sql")


@pytest.fixture(scope="session")
def start_app(xprocess):
    """Start `npm run dev` for the RedwoodSDK project and wait for port 5173."""

    class Starter(ProcessStarter):
        name = "rwsdk_app"
        args = ["npm", "run", "dev"]
        env = os.environ.copy()
        popen_kwargs = {
            "cwd": PROJECT_DIR,
            "text": True,
        }
        timeout = 300
        terminate_on_interrupt = True

        def startup_check(self):
            with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
                if s.connect_ex(("localhost", 5173)) != 0:
                    return False
            try:
                r = requests.get(f"{BASE_URL}/api/users", timeout=5)
                return r.status_code == 200
            except requests.RequestException:
                return False

    xprocess.ensure(Starter.name, Starter)
    yield
    info = xprocess.getinfo(Starter.name)
    info.terminate()



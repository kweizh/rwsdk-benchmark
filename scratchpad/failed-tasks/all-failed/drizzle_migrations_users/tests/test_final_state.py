def test_list_users_shape_and_created_at(start_app):
    now_ms = int(time.time() * 1000)
    r = requests.get(f"{BASE_URL}/api/users", timeout=15)
    assert r.status_code == 200, f"Expected 200 from list users, got {r.status_code}: {r.text}"
    users = r.json()
    assert isinstance(users, list), f"Expected list, got {type(users).__name__}"
    by_email = {u.get("email"): u for u in users}
    assert "alice@example.com" in by_email, f"alice@example.com missing in {users}"
    assert "bob@example.com" in by_email, f"bob@example.com missing in {users}"

    alice = by_email["alice@example.com"]
    bob = by_email["bob@example.com"]
    assert alice.get("displayName") == "Alice", f"Alice displayName mismatch: {alice}"
    assert bob.get("displayName") is None, f"Bob displayName should be null: {bob}"

    for u in (alice, bob):
        assert isinstance(u.get("id"), int), f"id should be int in {u}"
        assert isinstance(u.get("email"), str), f"email should be string in {u}"
        assert isinstance(u.get("createdAt"), int), f"createdAt should be int (epoch ms) in {u}"
        # createdAt within last 10 minutes (in ms) of the now timestamp
        delta = abs(now_ms - u["createdAt"])
        assert delta <= 10 * 60 * 1000, (
            f"createdAt for {u['email']} not within 10 minutes of now "
            f"(delta={delta}ms, createdAt={u['createdAt']}, now={now_ms})"
        )



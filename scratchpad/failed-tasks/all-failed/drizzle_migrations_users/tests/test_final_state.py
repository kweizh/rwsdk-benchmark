def test_register_first_user(start_app):
    payload = {
        "email": "alice@example.com",
        "password": "correct horse battery staple",
        "displayName": "Alice",
    }
    r = requests.post(f"{BASE_URL}/api/auth/register", json=payload, timeout=15)
    assert r.status_code == 201, f"Expected 201 from register, got {r.status_code}: {r.text}"
    data = r.json()
    assert isinstance(data.get("id"), int), f"id should be int, got: {data}"
    assert data.get("email") == "alice@example.com", f"Unexpected email: {data}"
    assert data.get("displayName") == "Alice", f"Unexpected displayName: {data}"


